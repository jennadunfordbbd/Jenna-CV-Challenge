function displayEachPage(pageNumber) {
  switch (pageNumber) {
    case 0:
      window.location.href = "./../Pages/homePage.html";
      break;
    case 1:
      window.location.href = "./../Pages/aboutPage.html";
      break;
    case 2:
      window.location.href = "./../Pages/experiencePage.html";
      break;
    case 3:
      window.location.href = "./../Pages/softwareProjects.html";
      break;
    case 4:
      window.location.href = "./../Pages/artwork.html";
      break;
  }
}
